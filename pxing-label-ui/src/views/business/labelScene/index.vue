<script>
import {listLabelScene, addLabelScene} from "@/api/business/labelScene"
import {listLabelProject} from "@/api/business/labelProject"
import {listLabelTask} from "@/api/business/labelTask";



export default {
  name: "Role",
  data() {
    return {
      // 遮罩层
      loading: true,
      // 导出遮罩层
      exportLoading: false,
      // 选中数组
      ids: [],
      // 非单个禁用
      single: true,
      // 非多个禁用
      multiple: true,
      // 显示搜索条件
      showSearch: true,
      // 总条数
      total: 0,
      // 弹出层标题
      title: "",
      // 是否显示弹出层
      open: false,
      // 是否显示弹出层（数据权限）
      openDataScope: false,
      menuExpand: false,
      menuNodeAll: false,
      deptExpand: true,
      deptNodeAll: false,
      // 日期范围
      dateRange: [],
      menuOptions: [],
      projectIds:[],
      projectOptions: [],

      taskOptions: [],
      projectDict:{},
      // 查询参数
      sceneList: [],
      queryParams: {
        pageNum: 1,
        pageSize: 10,
        sceneName: undefined
      },
      // 表单参数
      form: {},
      defaultProps: {
        children: "children",
        label: "label"
      },
      // 表单校验
      rules: {
        sceneName: [
          { required: true, message: "项目名称不能为空", trigger: "blur" }
        ]
      }
    };
  },
  created() {
    this.getList();
  },
  methods: {
    /** 查询任务列表 */
    getList() {
      listLabelProject().then(response => {
        this.projectOptions= response.rows;
        this.reset();
        //this.open = true;
        this.title = "添加场景";
      });
      this.loading = false;
      listLabelScene(this.addDateRange(this.queryParams, this.dateRange)).then(
        response => {
          this.sceneList= response.rows;
          this.total = response.total;
          this.loading = false;
        }
      );
    },
    projectFormat(row,column){
      //alert(row.id);
      for(var index in this.projectOptions){
        var project= this.projectOptions[index];
        if(project.id== row.projectId)
             return project.projectName;
        }
    },
    /** 新增按钮操作 */
    handleAdd() {
      //listLabelProject().then(response => {
        //this.projectOptions= response.rows;
      listLabelTask(this.addDateRange(this.queryParams, this.dateRange)).then(response =>{
        this.taskOptions= response
        this.reset();
        this.open = true;
        this.title = "添加场景";
      });
    },
    // 取消按钮
    cancel() {
      this.open = false;
      this.reset();
    },
    // 取消按钮（数据权限）
    cancelDataScope() {
      this.openDataScope = false;
      this.reset();
    },
    // 表单重置
    reset() {
      if (this.$refs.menu != undefined) {
        this.$refs.menu.setCheckedKeys([]);
      }
      this.menuExpand = false,
      this.menuNodeAll = false,
      this.deptExpand = true,
      this.deptNodeAll = false,
      this.form = {
        limitSIze: 100,
        inputSIze: 10
      };
      this.resetForm("form");
    },
    /** 搜索按钮操作 */
    handleQuery() {
      this.queryParams.pageNum = 1;
      this.getList();
    },
    /** 重置按钮操作 */
    resetQuery() {
      this.dateRange = [];
      this.resetForm("queryForm");
      this.handleQuery();
    },
    /** 提交按钮 */
    submitForm: function() {
      this.$refs["form"].validate(valid => {
        if (valid) {
          if (this.form.id != undefined) {

          } else {
            addLabelScene(this.form).then(response => {
              this.msgSuccess("新增成功");
              this.open = false;
              this.getList();
            });
          }
        }
      });
    },
  }
};
</script>
